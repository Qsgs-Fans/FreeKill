| 玩家人数 | 主公 | 忠臣 | 反贼 | 内奸 |
| -------- | ---- | ---- | ---- | ---- |
| 2        | 1    | 0    | 1    | 0    |
| 3        | 1    | 0    | 1    | 1    |
| 4        | 1    | 1    | 1    | 1    |
| 5        | 1    | 1    | 2    | 1    |
| 6        | 1    | 1    | 3    | 1    |
| 7        | 1    | 2    | 3    | 1    |
| 8        | 1    | 2    | 4    | 1    |

