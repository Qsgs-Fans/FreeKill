## 游戏配件

游戏需要用到的牌为：身份牌，体力牌，武将牌，游戏牌。

身份牌8张：1主公，2忠臣，4反贼，1内奸；武将牌25张；游戏牌104张＋EX游戏牌4张；体力牌8张。

___

## 游戏目标

玩家的游戏目标是由拿到的身份牌决定的，每种身份的胜利条件如下：

- 主公：消灭所有的反贼和内奸，平定天下。
- 忠臣：不惜一切代价保护主公，胜利条件与主公相同。
- 反贼：杀死主公，推翻统治。
- 内奸：除掉除自己之外的所有人，成为最后的生还者。

