# 身份模式简介

___

（原文地址： https://sgs.52pk.com/zl/201205/5299813.shtml ）

你即将开始学习一款集角色扮演、战斗、伪装等要素于一体的多人卡牌游戏。它能让你通过扮演耳熟能详的三国角色，在颠覆性的历史舞台中，演义一段扑朔迷离并充满刺激的较量。你将会充分体验到与玩家博弈的乐趣，它将是你聚会休闲的最佳伙伴，它就是――三国杀。

