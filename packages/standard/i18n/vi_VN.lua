# Giới thiệu chế độ thân phận

___

Bạn sắp bắt đầu học cách chơi một trò chơi thẻ bài nhiều người chơi, kết hợp các yếu tố nhập vai, chiến đấu và ngụy trang. Bạn sẽ được hóa thân thành những nhân vật nổi tiếng từ thời Tam Quốc, tham gia vào một vở kịch sân khẩu, diễn lại một trận đấu đầy bí ẩn, kích thích. Bạn sẽ được trải nghiệm trọn vẹn niềm vui khi đấu trí cùng những người chơi khác, điều đó khiến trò chơi này trở thành người đồng hành lý tưởng cho những buổi gặp mặt thư gián với bạn bè, đó chính là ——Tam Quốc Sát.

___

## Phụ kiện trò chơi

Các loại bài cần dùng trong trò chơi: Lá thân phận, thanh máu, lá tướng, lá bài đánh.

8 lá thân phận: 1 Chủ công, 2 Trung thần, 4 Phản tặc, 1 Nội Gián; 25 lá tướng; 104 lá bài đánh + 4 lá EX; 8 thanh máu.

___

## Mục tiêu trò chơi

Mục tiêu của người chơi được quyết định dựa trên lá thân phận mà họ nhận được, điều kiện thắng của mỗi thân phận như sau:

- Chủ công: Giết chết tất cả Phản tặc và Nội gián, bình định thiên hạ.
- Trung thần: Bảo về Chủ công bằng mọi giá, điều kiện thắng giống Chủ công.
- Phản tặc: Giết chết Chủ công, lật đổ vương triều.
- Nội gián: Giết chết tất cả những người khác trừ bản thân, trở thành người sống sót cuối cùng.

___

## Xác định thân phận

Tham khảo bảng dưới đây, lấy ra các lá thân phận bằng số lượng người chơi:

| Số người | Chủ công | Trung thần | Phản tặc | Nội gián |
| -------- | -------- | -----------| -------- | -------- |
| 2 | 1 | 0 | 1 | 0 |
| 3 | 1 | 0 | 1 | 1 |
| 4 | 1 | 1 | 1 | 1 |
| 5 | 1 | 1 | 2 | 1 |
| 6 | 1 | 1 | 3 | 1 |
| 7 | 1 | 2 | 3 | 1 |
| 8 | 1 | 2 | 4 | 1 |

Phát ngẫu nhiên 1 lá thân phận cho mỗi người, người chơi rút được lá Chủ công phải lập tức lật lên (đặt lá Chủ công ngửa lên trước mặt), người có thân phận khác giữ bí mật lá của mình, không để người khác biết thân phận của mình.

___

## Chọn tướng

Lá tướng với các kỹ năng đặc biệt giúp trò chơi thêm phong phú và thú vị, đồng thời cũng khiến trò chơi phức tạp hơn, nếu đây là lần đầu bạn chơi Tam Quốc Sát, bạn có thể tạm thời không sử dụng lá tướng. Hãy bỏ qua phần này, tiếp tục xem “phát thanh máu”. Nếu đã hiểu sơ qua về Tam Quốc Sát, bạn có thể thử thêm tướng và trò chơi, thực hiện các bước sau để chọn tướng.

Trước tiên, đưa cho người chơi có thân phận Chủ công (gọi tắt là Chủ công) “Tào Tháo”、“Lưu Bị”、“Tôn Quyền”, và thêm 2 tướng được rút ngẫu nhiên khác, tổng cộng 5 lá tướng. Chủ công chọn 1 tướng để vào vai, mở lá tướng lên cho mọi người xem.

Xáo trộn 24 tướng còn lại, phát ngẫu nhiên cho mỗi người còn lại 3 lá（trong ván 10 người, mỗi người được phát 2 lá）. Tiếp đó, mỗi người chọn 1 tướng trong số 3 tướng được phát, úp trước mặt mình, sau khi tất cả mọi người để chọn xong, đồng loạt mở tướng lên. Xáo trộn những lá tướng chưa được chọn, đặt úp bên cạnh.

___

## Phát thanh máu

Lấy thanh máu tương ứng với giới hạn máu của tướng （xem số lượng biểu tượng âm dương）, dùng lá tướng che phần bên trái.

Chủ công được thêm 1 điểm vào giới hạn máu của mình （trừ khi chơi 4 người thì không được cộng）.

Ví dụ người có 3 giới hạn máu khi là chủ công, giới hạn máu của họ sẽ là 4, và sử dụng thanh máu 4 điểm.

Đặt thanh máu dưới lá tướng, để lộ số điểm tương đương số máu hiện tại. Khi máu giảm, di chuyển lá tướng sang phải để che phần máu bị giảm.

___

## Quy trình lượt chơi

Xáo trộn ngẫu nhiên bài đánh, chia cho mỗi người 4 lá, đây là bài khởi đầu trên tay（bài trên tay: Bài mà bạn cầm trên tay）.

Đặt các lá bài đánh còn lại ở giữa bàn, tạo thành chồng bài rút（các lá bài bị người chơi bỏ đi sẽ đặt ở bên cạnh, tạo thành chồng bài bỏ, các lá trong chồng bài bỏ được ngửa mặt lên）.

Khi chơi, lượt bắt đầu từ Chủ công, tiến hành các lượt chơi theo hướng ngược chiều kim đồng hồ.

Tức là: Mỗi người lần lượt có lượt riêng, sau khi một người kết thúc lượt, người chơi bên phải sẽ bắt đầu lượt tiếp theo, lượt chơi diễn ra theo tuần tự.

Lượt của mỗi người chia thành 6 giai đoạn:

1. Giai đoạn chuẩn bị

2. Giai đoạn phán xét

3. Giai đoạn rút bài

4. Giai đoạn ra bài

5. Giai đoạn bỏ bài

6. Giai đoạn kết thúc

Dưới đây là mô tả của từng giai đoạn:

### Giai đoạn chuẩn bị

Thông thường có thể bỏ qua, một số tướng có thể phát động kỹ năng trong giai đoạn này.

### Giai đoạn phán xét

Nếu bạn có lá công cụ thời gian trong khu phán xét, bạn lần lượt tiến hành phán xét những lá công cụ thời gian này.

*Nếu trước mặt bạn có hai công cụ thời gian trở lên, bạn bắt đầu phán xét từ công cụ được đặt vào sau cùng（công cụ được đặt đầu tiên thì phán xét cuối cùng）.*

### Giai đoạn rút bài

Bạn rút 2 lá đầu chồng bài rút.

*Trong trò chơi, nếu không có ghi gì đặc biệt, “rút... Lá bài” chỉ việc rút bài từ đỉnh của chồng bài rút.*

*Khi cần rút bài hoặc chuẩn bị ảnh hưởng đến bộ bài, nếu chồng bài rút không còn bài, ngay lập tức trộn bài từ chồng bài bỏ để tạo thành chồng bài rút mới.*

### Giai đoạn ra bài

Bạn có thể sử dụng bài tùy ý, để bổ sung sức mạnh cho bản thân hoặc tấn công người khác, bạn cần tuân thủ 2 quy tắc:

1. Mỗi giai đoạn ra bài chỉ được sử dụng 【Sát】 một lần.

2. Vùng phán xét/trang bị của bất kỳ người nào cũng không được có hai lá bài cùng tên.

Sử dụng 1 lá, tức là chấp hành hiệu quả của lá đó, xem chi tiết tại “diễn giải bài đánh”. Nếu không có lưu ý đặc biệt, lá bài sau khi sử dụng phải bỏ đi（đặt vào chồng bài bỏ）.

### Giai đoạn bỏ bài

Trong giai đoạn ra bài, khi bạn không thể chơi bài hoặc không muốn tiếp tục chơi bài, tiến vào giai đoạn bỏ bài, lúc này, kiểm tra số lượng bài trên tay có lớn hơn máu hiện tại hay không（giới hạn trữ bài bằng máu hiện tại của bạn）, với mỗi lá vượt quá, bạn phải bỏ đi 1 lá.

### Giai đoạn kết thúc

Thông thường có thể bỏ qua, một số tướng có thể phát động kỹ năng trong giai đoạn này.

___

## Thông tin bổ sung

1. Trong trò chơi, nếu không có ghi gì đặc biệt, rút bài có nghĩa là rút từ đỉnh của chồng bài rút.

2. Những lá bài mà người chơi sử dụng, đánh ra hoặc bỏ trong trò chơi sẽ được đặt sang một bên, tạo thành chồng bài bỏ, các lá trong chồng bài bỏ được ngửa mặt lên.

3. Khi chồng bài rút không còn bài, ngay lập tức trộn chồng bài bỏ để tạo thành chồng bài mới.

4. “Giới hạn máu” và “số máu hiện tại” không giống nhau, cần chú ý phân biệt.

___

## Hấp hối và chết

Khi số máu của 1 người giảm xuống 0 hoặc thấp hơn, lập tức tiến vào trạng thái hấp hối, trư khi bản thân hoặc người khác sử dụng 【Đào】 để cứu người đó vào lúc này, nếu không người đó sẽ chết. Sau khi 1 người chết, họ bỏ tất cả bài và bài trong vùng phán xét, đồng thời lần ngửa lá thân phận.

Bất kỳ ai giết 1 phản tặc (kể cả kẻ đó cũng là phản tặc), ngay lập tức được rút 3 lá.

Nếu chủ công giết trung thần, chủ công phải lập tức bỏ tất cả bài trên tay và bài đã trang bị.

___

## Kết thúc trò chơi

Khi 1 trong những trường hợp sau xảy ra, trò chơi lập tức kết thúc:

1. Chủ công bị giết. Nếu lúc này Gian thần là người duy nhất còn sống（và chỉ còn 1 gian thần duy nhất）, Gian thần đó chiến thắng, trong các trường hợp khác, phản tặc chiến thắng（bất kể các phản tặc còn sống hay không）.

2. Tất cả Phản tặc và Gian thần đều đã chết: Chủ công và Trung thần（bất kể còn sống hay đã chết）đều giành chiến thắng.

